package Dayone;

public class DataTypesDemo {

	public static void main(String[] args) {
		int v1 = 9 / 2; 
		float v2 = 100 / 61;
		double v3 = 10d / 6d;
		float v4 = 6/2.5f; 
		System.out.println("value 1=" + v1); 
		System.out.println("value 2=" + v2);
		System.out.println("value 3=" + v3);
		System.out.println("value 4=" + v4);
		int marker = 512;
		double percentage = marker * 0.46f;
		System.out.println("percentage :" + percentage);

	}

}