package Dayone;

public class charDemo {

	public static void main(String[] args) {
		char ch = 'b';
		System.out.println(ch);
		char ch1 = 65;
		System.out.println(ch1);
		char v1 = '\u08A7';
		System.out.println(v1);
		char v2 = '\u20AC';
		System.out.println(v2);
		int a = 'B';
		System.out.println(a);
	}

}